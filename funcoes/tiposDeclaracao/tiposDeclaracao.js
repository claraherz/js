console.log(soma(3, 4))

// function declaration
function soma(x,y){
    return x + y
}

// function expression
const sub = function (x,y){
    return x - y
}

// named function expression
const mult = function mult(x, y){
    return x * y
}

//na function expression (os dois jeitos) é só possivel chama a função depois do codigo desta, diferente da function declaration, que pode ser chamada antes ou depois da(s) linha(s) de comando